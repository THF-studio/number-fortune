function sumDigits(n: number): { digits: number[]; sum: number } {
  const digits = String(n).split('').map(Number)
  return { digits, sum: digits.reduce((a, b) => a + b, 0) }
}

export function calculateLifePath(year: string, month: string, day: string): LifePathResult | null {
  const y = parseInt(year, 10)
  const m = parseInt(month, 10)
  const d = parseInt(day, 10)
  if (isNaN(y) || isNaN(m) || isNaN(d)) return null
  if (y < 1000 || y > 2100 || m < 1 || m > 12 || d < 1 || d > 31) return null

  const dateString = String(y) + String(m).padStart(2, '0') + String(d).padStart(2, '0')
  const allDigits = dateString.split('').map(Number)

  const reductionSteps: string[] = []

  let current = allDigits.reduce((a, b) => a + b, 0)
  reductionSteps.push(allDigits.join('+') + '=' + current)

  while (current > 9 && current !== 11 && current !== 22) {
    const { digits, sum } = sumDigits(current)
    reductionSteps.push(digits.join('+') + '=' + sum)
    current = sum
  }

  const lifePathNumber = current

  const digitCount: Record<number, number> = {}
  for (const n of allDigits) {
    if (n >= 1 && n <= 9) digitCount[n] = (digitCount[n] ?? 0) + 1
  }
  const presentSet = new Set(Object.keys(digitCount).map(Number))
  const presentDigits = Array.from(presentSet).sort((a, b) => a - b)
  const missingDigits = [1, 2, 3, 4, 5, 6, 7, 8, 9].filter(n => !presentSet.has(n))

  const digitPresence: DigitPresence[] = [1, 2, 3, 4, 5, 6, 7, 8, 9].map(n => {
    let status: DigitPresence['status']
    if (lifePathNumber <= 9 && n === lifePathNumber) {
      status = 'lifepath'
    } else if (presentSet.has(n)) {
      status = 'present'
    } else {
      status = 'missing'
    }
    return { digit: n, count: digitCount[n] ?? 0, status, traitNote: DIGIT_TRAIT_NOTES[n] }
  })

  // Personal year: reduce(birth month + birth day digits + current year digits)
  const currentYear = new Date().getFullYear()
  const pyDigits = [...String(m).split(''), ...String(d).split(''), ...String(currentYear).split('')].map(Number)
  let py = pyDigits.reduce((a, b) => a + b, 0)
  while (py > 9 && py !== 11 && py !== 22) {
    py = String(py).split('').map(Number).reduce((a, b) => a + b, 0)
  }
  const personalYear = py

  const profile = LIFE_PATH_PROFILES[lifePathNumber]
  if (!profile) return null

  return {
    dateString,
    lifePathNumber,
    personalYear,
    reductionSteps,
    presentDigits,
    missingDigits,
    profile,
    digitPresence,
  }
}
